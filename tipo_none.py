"""
Tipo None

O tipo de dado None em python representa o tipo sem tipo
ou pode ser conhecido também como tipo vazio, porém falar que é um tipo
sem tipo é mais apropriado

O tipo none é sempre especificado com a primeira letra maiscula: None

Quando utilizamos?

- Podemos utilizar o None quando queremos criar uma variavel e inicializa-la com um tipo sem
tipo, antes de recebermos um valor fnal

OBS: o tipo none em python é sempre considerado como falso
"""
# -*- coding: UTF-8 -*-
numeros = None
print(numeros)
print(type(None))
print('\n')

numeros = 1.44, 1.34, 5.67
print(numeros)
print(type(numeros))